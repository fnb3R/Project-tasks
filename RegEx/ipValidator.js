// Да се валидира IP адрес. IP адресът се състои от 4 секции от числа от 0 до 255.

function isValidIP(ip) {
    const regex = /^(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}$/;
    return regex.test(ip);
}

// Тестови примери
const ips = [
    "0.0.0.0",
    "8.8.8.8",
    "255.255.255.255",
    "192.168.1.1",
    "999.999.999.999", // Невалиден
    "256.0.0.1",       // Невалиден
    "123.456.78.90",   // Невалиден
];

// Извеждаме резултатите
ips.forEach(ip => {
    if (isValidIP(ip)) {
        console.log(`${ip} е валиден IP адрес.`);
    } else {
        console.log(`${ip} е невалиден IP адрес.`);
    }
});
